#pragma once
#include <cstdint> // uint16_t
#include <vector>

// コードパターンのデータ構造
struct ChordPattern {
  uint16_t pattern;
  const char *name;
  float weight; // パターンの重み（優先度）
};

// コードパターンのリストを返すインライン関数
inline std::vector<ChordPattern> getChordPatternsData() {
  std::vector<ChordPattern> patterns = {
      // 基本コード
      {0b000010010001, "maj", 1.0},  // 1 3 5
      {0b000010001001, "min", 1.0},  // 1 b3 5
      {0b000010000101, "sus4", 0.9}, // 1 4 5
      {0b000010000011, "sus2", 0.9}, // 1 2 5
      {0b000010000001, "5", 0.8},    // 1 5

      // 7thコード
      {0b010010010001, "7", 1.0},     // 1 3 5 b7
      {0b010010001001, "min7", 1.0},  // 1 b3 5 b7
      {0b100010010001, "maj7", 0.95}, // 1 3 5 7

      // add系（3和音＋テンション、b7を含まない）
      {0b000010010101, "add9", 0.80},  // 1 3 5 9
      {0b000010010111, "add11", 0.75}, // 1 3 5 11
      {0b000010010011, "add13", 0.75}, // 1 3 5 13 (追加)

      // 9thコード
      {0b010010010101, "9", 0.9},    // 1 3 5 b7 9
      {0b010010001101, "min9", 0.9}, // 1 b3 5 b7 9
      {0b110010010101, "maj9", 0.9}, // 1 3 5 7 9

      // 6thコード
      {0b000010010011, "6", 0.85},    // 1 3 5 6
      {0b000010001011, "min6", 0.85}, // 1 b3 5 6
      {0b000010010111, "6/9", 0.75},  // 1 3 5 6 9 (追加)

      // 特殊コード
      {0b000010001010, "dim", 0.85},  // 1 b3 b5
      {0b010010001010, "m7b5", 0.85}, // 1 b3 b5 b7（ハーフディミニッシュ）
      {0b001001001010, "dim7", 0.85}, // 1 b3 b5 bb7（ディミニッシュ7th）
      {0b000100010001, "aug", 0.85},  // 1 3 #5
      {0b010100010001, "7#5", 0.85},  // 1 3 #5 b7

      // susバリエーション (追加)
      {0b000010000111, "sus2/4", 0.7}, // 1 2 4 5
      {0b010010000101, "7sus4", 0.85}, // 1 4 5 b7
      {0b010010000111, "9sus4", 0.8},  // 1 4 5 b7 9

      // 11thコード
      {0b010010010111, "11", 0.8},    // 1 3 5 b7 9 11
      {0b010010001111, "min11", 0.8}, // 1 b3 5 b7 9 11

      // 13thコード
      {0b010010010011, "13", 0.8},     // 1 3 5 b7 13
      {0b010010001011, "min13", 0.8},  // 1 b3 5 b7 13
      {0b100010010011, "maj13", 0.75}, // 1 3 5 7 13 (追加)
      {0b010010011111, "13", 0.75},    // 1 3 5 b7 9 11 13（完全形）

      // 変則的なテンション (追加)
      {0b010010010011, "7b9", 0.8}, // 1 3 5 b7 b9
      {0b010010011001, "7#9", 0.8}  // 1 3 5 b7 #9
  };
  return patterns;
}
