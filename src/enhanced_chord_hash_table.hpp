// Binary search lookup function
inline const EnhancedChordEntry* findEnhancedChord(uint16_t mask) {
    int left = 0;
    int right = ENHANCED_CHORD_TABLE_SIZE - 1;
    
    while (left <= right) {
        int mid = left + (right - left) / 2;
        
        if (ENHANCED_CHORD_TABLE[mid].mask == mask) {
            return &ENHANCED_CHORD_TABLE[mid];
        } else if (ENHANCED_CHORD_TABLE[mid].mask < mask) {
            left = mid + 1;
        } else {
            right = mid - 1;
        }
    }
    
    return nullptr;
}
